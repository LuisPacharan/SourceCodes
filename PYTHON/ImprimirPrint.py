#Funcion Print para imprimir en consola

nombre   = 'Luis'
apellido = 'Enrique'

#El parametro sep nos ayuda a definir como queremos separar los elementos del print
print(nombre, apellido, 'Arana', 31, (1,2,3), sep = ' ')
print(nombre, apellido, 'Arana', 31, (1,2,3), sep = ' - ')
print(nombre, apellido, 'Arana', 31, (1,2,3), sep = ' / ')